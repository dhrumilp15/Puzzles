class Solution:
    def solve(self, matrix):
        # Write your code here
        dplist = [[0 for _ in range(len(matrix[0]) + 1)] for __ in range(len(matrix) + 1)]
        for row in range(1, len(matrix) + 1):
            for col in range(1, len(matrix[0]) + 1):
                dplist[row][col] = max(dplist[row - 1][col], dplist[row][col-1]) + matrix[row-1][col-1]
        return dplist[-1][-1]

